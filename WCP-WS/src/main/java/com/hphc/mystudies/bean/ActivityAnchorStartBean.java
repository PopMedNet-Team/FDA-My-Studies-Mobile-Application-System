package com.hphc.mystudies.bean;

public class ActivityAnchorStartBean {

	private Integer anchorDays = 0;
	private Integer dayOfWeek = 0;
	private String dateOfMonth = "";
	private String time = "";
	
	public Integer getAnchorDays() {
		return anchorDays;
	}
	public void setAnchorDays(Integer anchorDays) {
		this.anchorDays = anchorDays;
	}
	public Integer getDayOfWeek() {
		return dayOfWeek;
	}
	public void setDayOfWeek(Integer dayOfWeek) {
		this.dayOfWeek = dayOfWeek;
	}
	public String getDateOfMonth() {
		return dateOfMonth;
	}
	public void setDateOfMonth(String dateOfMonth) {
		this.dateOfMonth = dateOfMonth;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
